// Fill missing languages with English fallback
const baseLangs = ["zh", "ja", "ko", "ar", "hi", "pt", "it", "de", "id"];
baseLangs.forEach(code => {
  if (!t[code]) t[code] = { ...t.en };
});

// Get translation — falls back to English if key missing
export function useTranslation(language = "en") {
  const lang = t[language] || t.en;
  return (key) => lang[key] || t.en[key] || key;
}
